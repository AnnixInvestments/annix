#!/usr/bin/env node
/**
 * Transform the ESCO skills CSV (downloaded manually from
 * https://esco.ec.europa.eu/en/use-esco/download) into a TypeORM seed
 * migration that inserts ~13k rows into cv_assistant_esco_skills.
 *
 * Usage:
 *   node scripts/cv-assistant/esco-csv-to-seed.mjs \
 *     --input ~/Downloads/v1.2.1/skills_en.csv \
 *     --output annix-backend/src/migrations/1820100000089-SeedCvEscoSkills.ts
 *
 * The script does not network. Download the CSV manually and pass its path.
 *
 * Schema reference (ESCO skills CSV v1.2.1, English):
 *   conceptType,conceptUri,skillType,reuseLevel,preferredLabel,
 *   altLabels,hiddenLabels,status,modifiedDate,scopeNote,definition,
 *   inScheme,description
 *
 * altLabels are pipe-delimited inside a single field, one per language tag
 * (`en`). We split on `\n` then trim and filter to non-empty.
 */

import { readFile, writeFile } from "node:fs/promises";
import { argv, exit } from "node:process";

async function main() {
  const args = parseArgs(argv.slice(2));
  if (!args.input || !args.output) {
    console.error("Usage: esco-csv-to-seed.mjs --input <path.csv> --output <migration.ts>");
    exit(2);
  }

  const csv = await readFile(args.input, "utf8");
  const rows = parseCsv(csv);
  if (rows.length === 0) {
    console.error("No rows parsed from CSV.");
    exit(1);
  }

  const skills = rows
    .filter((r) => (r.conceptType ?? "").toLowerCase() === "knowledgeskillcompetence")
    .map((r) => ({
      uri: (r.conceptUri ?? "").trim(),
      preferredLabel: (r.preferredLabel ?? "").trim(),
      altLabels: (r.altLabels ?? "")
        .split(/[\n\r]+/u)
        .map((s) => s.trim())
        .filter((s) => s.length > 0),
      description: ((r.description ?? "") || (r.definition ?? "")).trim() || null,
    }))
    .filter((s) => s.uri.length > 0 && s.preferredLabel.length > 0);

  console.log(`Parsed ${skills.length} skills from ${rows.length} CSV rows.`);

  const timestamp = "1820100000089";
  const className = `SeedCvEscoSkills${timestamp}`;

  const inserts = [];
  const CHUNK = 200;
  for (let i = 0; i < skills.length; i += CHUNK) {
    const slice = skills.slice(i, i + CHUNK);
    const values = slice
      .map((s) => {
        const uri = quoteSqlString(s.uri);
        const pref = quoteSqlString(s.preferredLabel);
        const altsJson = quoteSqlString(JSON.stringify(s.altLabels));
        const desc = s.description ? quoteSqlString(s.description) : "NULL";
        return `        (${uri}, ${pref}, ${altsJson}::jsonb, ${desc})`;
      })
      .join(",\n");
    inserts.push(`    await queryRunner.query(\`
      INSERT INTO "cv_assistant_esco_skills" ("esco_uri", "preferred_label", "alt_labels", "description")
      VALUES
${values}
      ON CONFLICT ("esco_uri") DO NOTHING
    \`);`);
  }

  const file = `import type { MigrationInterface, QueryRunner } from "typeorm";

/**
 * Generated by scripts/cv-assistant/esco-csv-to-seed.mjs.
 * Source: ESCO v1.2.1 English skills classification.
 * Licence: CC-BY 4.0 (https://esco.ec.europa.eu/en/about-esco/legal-notice).
 *
 * Do NOT hand-edit. Re-run the transformer to regenerate.
 */
export class ${className} implements MigrationInterface {
  name = "${className}";

  async up(queryRunner: QueryRunner): Promise<void> {
${inserts.join("\n\n")}
  }

  async down(queryRunner: QueryRunner): Promise<void> {
    await queryRunner.query('DELETE FROM "cv_assistant_esco_skills"');
  }
}
`;

  await writeFile(args.output, file, "utf8");
  console.log(`Wrote ${args.output} (${skills.length} skills in ${inserts.length} chunks).`);
}

function parseArgs(args) {
  const out = {};
  for (let i = 0; i < args.length; i++) {
    if (args[i] === "--input") {
      out.input = args[i + 1];
      i++;
    } else if (args[i] === "--output") {
      out.output = args[i + 1];
      i++;
    }
  }
  return out;
}

function quoteSqlString(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function parseCsv(text) {
  const rows = [];
  let i = 0;
  let field = "";
  let row = [];
  let inQuotes = false;
  const len = text.length;
  while (i < len) {
    const ch = text[i];
    if (inQuotes) {
      if (ch === '"') {
        if (text[i + 1] === '"') {
          field += '"';
          i += 2;
          continue;
        }
        inQuotes = false;
        i++;
        continue;
      }
      field += ch;
      i++;
      continue;
    }
    if (ch === '"') {
      inQuotes = true;
      i++;
      continue;
    }
    if (ch === ",") {
      row.push(field);
      field = "";
      i++;
      continue;
    }
    if (ch === "\r") {
      i++;
      continue;
    }
    if (ch === "\n") {
      row.push(field);
      rows.push(row);
      field = "";
      row = [];
      i++;
      continue;
    }
    field += ch;
    i++;
  }
  if (field.length > 0 || row.length > 0) {
    row.push(field);
    rows.push(row);
  }

  if (rows.length === 0) return [];
  const headers = rows[0];
  return rows.slice(1).map((cols) => {
    const obj = {};
    headers.forEach((h, idx) => {
      obj[h.trim()] = cols[idx] ?? "";
    });
    return obj;
  });
}

main().catch((err) => {
  console.error(err);
  exit(1);
});
